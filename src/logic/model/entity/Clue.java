package logic.model.entity;

public class Clue {
	
	private int clueIndex;
	
	private String text;
	
	public int getClueIndex() {
		return clueIndex;
	}

	public void setClueIndex(int clueIndex) {
		this.clueIndex = clueIndex;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	
}
